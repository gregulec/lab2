#include<iostream>
using namespace std;

int main() {
	cout << "Bool " << sizeof(bool) << "B" << endl;
	cout << "Char " << sizeof(char) << "B" << endl;
	cout << "Int " << sizeof(int) << "B" << endl;
	cout << "Short " << sizeof(short) << "B" << endl;
	cout << "Long " << sizeof(long) << "B" << endl;
	cout << "Float " << sizeof(float) << "B" << endl;
	cout << "Double " << sizeof(double) << "B" << endl;
	cout << "Signed Char " << sizeof(signed char) << "B" << endl;
	cout << "Unsigned Char " << sizeof(unsigned char) << "B" << endl;
	cout << "LongInt " << sizeof(long int) << "B" << endl;
	cout << "Short Int " << sizeof(short int) << "B" << endl;
}